let form = document.getElementById("form");

// Cria o formulário dinamicamente
questions.forEach((question, index) => {
  const questionHTML = `
        <div class="question" key="${index}">
            <p>${index + 1}. ${question.question}</p>
            <div class="options">
                ${question.respostas
                  .map(
                    (resposta, i) => `
                    <label>
                        <input type="radio" name="question${index}" value="${resposta.value}" >
                        ${resposta.text}
                    </label>
                `
                  )
                  .join("")}
            </div>
        </div>`;
  form.innerHTML += questionHTML;
});

// Adiciona o botão de submit
form.innerHTML += `<button class="submit-button" type="submit">Enviar Respostas</button>`;

// Adiciona o evento de submit
form.addEventListener("submit", (event) => {
  event.preventDefault();
  console.log(event);

  // Valida se todas as perguntas foram respondidas
  let todasRespondidas = true;
  questions.forEach((question, index) => {
    const selected = document.querySelector(
      `input[name="question${index}"]:checked`
    );
    if (!selected) {
      todasRespondidas = false;
    }
  });

  if (!todasRespondidas) {
    alert("Por favor, responda todas as perguntas antes de enviar.");
    return;
  }

  // Calcula a pontuação
  let score = 0;
  const results = [];

  questions.forEach((question, index) => {
    const selected = document.querySelector(
      `input[name="question${index}"]:checked`
    );
    if (selected) {
      const respostaSelecionada = selected.value;
      const respostaCorreta = gabarito.find(
        (g) => g.name === `q${index + 1}`
      ).correta;

      if (respostaSelecionada === respostaCorreta) {
        score++;
      }

      results.push({
        pergunta: question.question,
        respostaSelecionada,
        respostaCorreta,
        acertou: respostaSelecionada === respostaCorreta,
      });
    }
  });

  // Exibe os resultados
  let nota = (score / questions.length) * 26;

  let passou = nota >= 6 ? "Aprovado!" : "Reprovado!";


  const toaster = document.getElementById("toaster");
  toaster.innerHTML = `
    <h2>Você acertou ${score} de ${questions.length} questões</h2>
    <h2>Nota: ${nota.toFixed(1)}</h2>
    <h2>${passou}</h2>
  `;
  form.appendChild(toaster);

  toaster.className = nota >= 6 ? "toaster aprovado" : " toaster reprovado";
  toaster.style.display = "block";
  toaster.style.opacity = "1";

  // Adiciona o botão de reset
  const resetButton = document.createElement("button");
  resetButton.textContent = "Tentar novamente";
  resetButton.className = "submit-button";
  resetButton.addEventListener("click", () => {
    window.location.reload();
  });

  toaster.appendChild(resetButton);
});


